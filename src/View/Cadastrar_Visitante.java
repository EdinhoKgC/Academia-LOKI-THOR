    private void listar() {

        try {
            VisitanteDao objvisitantedao = new VisitanteDao();

            DefaultTableModel model = (DefaultTableModel) tabelaVisitante.getModel();
            model.setNumRows(0);

            ArrayList<Visitante> lista = objvisitantedao.Pesquisar();

            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getId(),
                    lista.get(num).getNome(),
                    lista.get(num).getCpf(),
                    lista.get(num).getValorpagar()
                });
            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Listar" + erro);
        }
    }
    
    private void CarregarCampos(){
        int setar = tabelaVisitante.getSelectedRow();
        txtID.setText(tabelaVisitante.getModel().getValueAt(setar, 0).toString());
        txtNome.setText(tabelaVisitante.getModel().getValueAt(setar, 1).toString());
        txtCPF.setText(tabelaVisitante.getModel().getValueAt(setar, 2).toString());
        txtValoraPagar.setText(tabelaVisitante.getModel().getValueAt(setar, 3).toString());
        
    }
    
    private void CadastrarVisitante(){
        String nome, cpf;
        int valorpagar;
        nome = txtNome.getText();
        cpf = txtCPF.getText();
        valorpagar = Integer.parseInt(txtValoraPagar.getText());
        
        if(valorpagar < 0){
           JOptionPane.showMessageDialog(rootPane, "isira um valor vÃ¡lido para: valor a pagar");
       }
        else{
        Visitante visitantes = new Visitante();
        visitantes.setNome(nome);
        visitantes.setCpf(cpf);
        visitantes.setValorpagar(valorpagar);

        VisitanteDao visitante = new VisitanteDao();
        visitante.save(visitantes);

        JOptionPane.showMessageDialog(rootPane, "Visitante Cadastrado!");
        }
    }
    
    private void LimparCampos(){
        txtID.setText("");
        txtNome.setText("");
        txtCPF.setText("");
        txtValoraPagar.setText("");
        txtNome.requestFocus();
    }
    
    private void AlterarVisitante(){
        String nome, cpf;
        int valorpagar, id;
        nome = txtNome.getText();
        cpf = txtCPF.getText();
        id = Integer.parseInt(txtID.getText());
        valorpagar = Integer.parseInt(txtValoraPagar.getText());
        
        
        Visitante visitantes = new Visitante();
        visitantes.setId(id);
        visitantes.setNome(nome);
        visitantes.setCpf(cpf);
        visitantes.setValorpagar(valorpagar);

        VisitanteDao visitante = new VisitanteDao();
        visitante.alterar(visitantes);

        JOptionPane.showMessageDialog(rootPane, "Dados Alterados!");
    }

    private void excluir(){
        int id;
        
        id = Integer.parseInt(txtID.getText());
        Visitante objvisitante = new Visitante();
        objvisitante.setId(id);
        
        VisitanteDao objvisitantedao = new VisitanteDao();
        objvisitantedao.excluir(objvisitante);
    }
    
